'use strict';

console.log('-- accessing characters in a string by index --');

// examples
console.log('eat'.length); // 3

console.log('eat'[0]); // 'e'
console.log('eat'[1]); // 'a'
console.log('eat'[2]); // 'l'

console.log('eat'[0] === 'e'); // true
console.log('eat'[1] === 'a'); // true
console.log('eat'[2] === 't'); // true

